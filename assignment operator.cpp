#include<iostream>
using namespace std;
int main()
{
	int a=10;
	cout<<"value of a:"<<a<<endl;
	a+=5;
	cout<<"value of a+=5:"<<a<<endl;
	a-=5;
	cout<<"value of a-=5:"<<a<<endl;
	a*=5;
	cout<<"value of a*=5:"<<a<<endl;
	a/=5;
	cout<<"value of a/=5:"<<a<<endl;
	a%=5;
	cout<<"value of a%=5:"<<a<<endl;
}
